desWavTab& WaveBank::operator[]( byte ith ) 
{
   static desWavTab buf;

   if ( ith >= num ) 
      ith = 0;

   /* copy descriptor from progmem to ram buffer */

   byte* rombyt = (byte *)dataPtr( ith );
   byte* rambyt = (byte *)&buf;

   for ( byte z = 0; z < sizeof( desWavTab ); z++ )
   {
      *rambyt++ = pgm_read_byte_near( rombyt++ );
   }

   return buf;
}

